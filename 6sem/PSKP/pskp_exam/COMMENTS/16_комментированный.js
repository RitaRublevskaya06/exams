// Импорт модуля http для создания HTTP сервера
const http = require('http');

// Создание HTTP сервера с обработчиком запросов
const server = http.createServer((req, res) => {
    // req - объект запроса, res - объект ответа
    
    // Простейшая маршрутизация: проверяем URL и метод запроса
    if (req.url === '/' && req.method === 'GET') {
        // Если запрос к корневому пути методом GET
        // Устанавливаем статус 200 и заголовок Content-Type
        res.writeHead(200, { 'Content-Type': 'text/plain; charset=utf-8' });
        // Отправляем текст ответа и завершаем соединение
        res.end('Главная страница');
    } else {
        // Для всех остальных маршрутов - генерация ответа с кодом 404
        // Устанавливаем статус 404 (Not Found)
        res.writeHead(404, { 'Content-Type': 'text/plain; charset=utf-8' });
        // Отправляем сообщение об ошибке
        res.end('404 Not Found: Запрашиваемый ресурс не существует');
    }
});

// Определение порта для сервера
const PORT = 3000;

// Запуск сервера на указанном порту
server.listen(PORT, () => {
    // Коллбэк функция, вызываемая при успешном запуске
    console.log(`Сервер запущен на http://localhost:${PORT}`);
});