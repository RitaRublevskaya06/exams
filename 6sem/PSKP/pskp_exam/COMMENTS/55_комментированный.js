// Задание 55 — NestJS + TypeORM (SQLite, без установки PostgreSQL)
// Это инструкция по запуску проекта NestJS с TypeORM, а не исполняемый файл

// TypeORM — это ORM (Object-Relational Mapping) для TypeScript и JavaScript,
// который работает с различными базами данных (PostgreSQL, MySQL, SQLite и др.)

// Инструкция по запуску:

// 1. Перейдите в папку проекта:
//   cd 55

// 2. Установите зависимости:
//   npm install
//   Будет установлено:
//   - NestJS
//   - TypeORM
//   - SQLite драйвер (не требует установки сервера SQLite)

// 3. Запустите сервер в режиме разработки:
//   npm run start:dev
//   Сервер запустится с hot-reload

// 4. Проверьте работу API:

//   GET запрос для получения всех пользователей:
//   http://localhost:3000/users

//   POST запрос для создания нового пользователя:
//   http://localhost:3000/users
//   Тело запроса (JSON): {"name": "John", "email": "john@example.com", "age": 25}

// База данных:
//   SQLite — это встраиваемая база данных, которая хранится в файле
//   Файл базы данных: users.db (создаётся автоматически при первом запуске)
//   Не требует установки отдельного сервера БД

// Структура проекта NestJS с TypeORM (в папке src/):

// 1. Сущности (Entities) — классы TypeScript, которые отображаются на таблицы БД:
//    - Определяют структуру таблиц
//    - Содержат декораторы для колонок, отношений, индексов
//    - Пример: user.entity.ts

// 2. Репозитории (Repositories) — паттерн для работы с данными:
//    - Предоставляют методы для CRUD операций
//    - В TypeORM репозитории создаются автоматически
//    - Инъекция репозитория через декоратор @InjectRepository()

// 3. Миграции (Migrations) — управление изменениями схемы БД:
//    - Версионирование структуры БД
//    - Откат изменений при необходимости
//    - Создаются через CLI TypeORM

// 4. Data Source / Connection — конфигурация подключения к БД:
//    - Настройки подключения (тип БД, хост, порт и т.д.)
//    - Регистрация сущностей
//    - Настройка миграций

// Примерная структура файлов в src/:

// main.ts              - Точка входа
// app.module.ts        - Корневой модуль с конфигурацией TypeORM
// users/
//   ├── user.entity.ts     - Сущность пользователя
//   ├── users.module.ts    - Модуль пользователей
//   ├── users.controller.ts - Контроллер
//   ├── users.service.ts    - Сервис (работает с репозиторием)
//   └── dto/
//        ├── create-user.dto.ts
//        └── update-user.dto.ts

// Пример сущности User:
// @Entity()                     // Декоратор сущности
// export class User {
//   @PrimaryGeneratedColumn()   // Первичный ключ с автоинкрементом
//   id: number;
//
//   @Column()                   // Обычная колонка
//   name: string;
//
//   @Column({ unique: true })   // Колонка с уникальным ограничением
//   email: string;
//
//   @Column({ nullable: true }) // Колонка, допускающая null
//   age: number;
//
//   @CreateDateColumn()         // Автоматическое заполнение даты создания
//   createdAt: Date;
// }

// Конфигурация TypeORM в app.module.ts:
// TypeOrmModule.forRoot({
//   type: 'sqlite',           // Тип БД
//   database: 'users.db',     // Имя файла БД
//   entities: [User],         // Сущности
//   synchronize: true,        // Автоматическая синхронизация схемы (только для разработки!)
// })

// Преимущества TypeORM:
// 1. Работа с различными БД через единый API
// 2. Поддержка TypeScript и декораторов
// 3. Автоматическая генерация SQL запросов
// 4. Поддержка миграций
// 5. Паттерн Repository для работы с данными

// Важное замечание:
// synchronize: true — удобно для разработки, но опасно для production!
// В production используйте миграции для управления схемой БД.