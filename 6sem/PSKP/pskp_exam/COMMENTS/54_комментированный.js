// NestJS — это прогрессивный фреймворк Node.js для построения эффективных
// и масштабируемых серверных приложений на TypeScript/JavaScript.

// Инструкция по запуску:

// 1. Перейдите в папку проекта:
//   cd 54

// 2. Установите зависимости:
//   npm install
//   Будет установлен NestJS и все необходимые зависимости из package.json

// 3. Запустите сервер в режиме разработки:
//   npm run start:dev
//   Этот скрипт запускает сервер с hot-reload (автоматическая перезагрузка при изменениях)

// 4. Проверьте работу API:

//   GET запрос для получения всех пользователей:
//   http://localhost:3000/users

//   GET запрос для получения пользователя по ID:
//   http://localhost:3000/users/1

//   POST запрос для создания нового пользователя:
//   http://localhost:3000/users
//   Тело запроса (JSON): {"name": "John", "email": "john@example.com", "age": 25}

// Структура проекта NestJS (в папке src/):

// 1. Модуль (Module) — организационная единица, которая объединяет:
//    - Контроллеры (Controllers)
//    - Сервисы (Services)
//    - Провайдеры (Providers)
//    - Другие модули

// 2. Контроллер (Controller) — обрабатывает HTTP запросы и возвращает ответы:
//    - Определяет маршруты (routes)
//    - Обрабатывает параметры запроса
//    - Вызывает сервисы для бизнес-логики

// 3. Сервис (Service) — содержит бизнес-логику приложения:
//    - Работа с данными
//    - Вычисления
//    - Взаимодействие с другими сервисами

// 4. Фильтр (Filter) — обработка исключений:
//    - Глобальная обработка ошибок
//    - Преобразование исключений в HTTP-ответы
//    - Кастомные сообщения об ошибках

// Примерная структура файлов в src/:

// main.ts              - Точка входа приложения
// app.module.ts        - Корневой модуль
// users/
//   ├── users.module.ts    - Модуль пользователей
//   ├── users.controller.ts - Контроллер пользователей
//   ├── users.service.ts    - Сервис пользователей
//   └── dto/                - Data Transfer Objects
//        ├── create-user.dto.ts  - DTO для создания пользователя
//        └── update-user.dto.ts  - DTO для обновления пользователя
// shared/
//   ├── filters/            - Фильтры исключений
//   └── interceptors/       - Интерцепторы

// Основные преимущества NestJS:
// 1. Архитектура, вдохновленная Angular
// 2. Встроенная поддержка TypeScript
// 3. Модульность и масштабируемость
// 4. Интеграция с множеством библиотек
// 5. Поддержка разных транспортов (HTTP, WebSocket, gRPC)